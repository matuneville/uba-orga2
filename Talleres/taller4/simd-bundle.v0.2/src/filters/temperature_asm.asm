global temperature_asm

section .rodata
	.align 16
    mascara: times 2 dq 0x0000FFFFFFFFFFFF
    
    div_3: times 4 dw 3

    ; o sino directamente 0x0000FFFFFFFFFFFF0000FFFFFFFFFFFF

section .text
;void Pintar_asm(unsigned char *src, 	[RDI]     
;              unsigned char *dst,		[RSI]
;              int width,				[RDX]
;              int height,				[RCX]
;              int src_row_size,		[R8] 
;              int dst_row_size);		[R9]

temperature_asm:

    push rbp
	mov rbp, rsp

	push rbx ; usamos RBX para offset de columna
	xor rbx, rbx

	push r12 ; R12 va a guardar width*4
	push r13 ; contador de linea

    mov r12, rdx
	mul r12, 4
	xor r13, r13

    xor xmm2, xmm2 ; inicializo ceros
    movdqu xmm6, [mascara] ; inicializo mascara para quitar Transparencia
    movdqu xmm7, [div_3] ; inicializo los 3 para division
    


    
    ciclo:
        movq xmm0, [rdi + rbx]
        movq xmm1, [rdi + rbx + 8]
        
        punpcklbw xmm0, xmm2 ;extendemos cada byte con 0
        punpcklbw xmm1, xmm2

        vptestmw xmm0, xmm6 ; hago un and bitwise con mascara
        vptestmw xmm1, xmm6
                                                                                                             
        phaddw xmm1, xmm0
        phaddw xmm1, xmm1

        

    ret




